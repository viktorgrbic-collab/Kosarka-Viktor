    public void popuniIgrace() {
        try {
            ArrayList<Igrac> igraci = ClientController.getInstance()
                    .getAllIgrac(new Igrac(null, "", "", 0));

            cmbIgrac.removeAllItems();

            for (Igrac igrac : igraci) {
                cmbIgrac.addItem(igrac);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void popuniLige() {
        try {
            ArrayList<Liga> lige = ClientController.getInstance()
                    .getAllLiga(new Liga(null, "", new Drzava(null, "")));

            cmbLiga.removeAllItems();

            for (Liga liga : lige) {
                cmbLiga.addItem(liga);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

