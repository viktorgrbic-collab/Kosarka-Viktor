    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "obrisete ovaj tim?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                ClientController.getInstance().deleteTim(t);
                FormPretragaTima fp = (FormPretragaTima) getParent();
                fp.refreshTable();
                JOptionPane.showMessageDialog(this, "Uspesno obrisan tim.");
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(FormDetaljiTima.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        try {

            if (txtNaziv.getText().isEmpty() || txtBrojPobeda.getText().isEmpty()
                    || txtBrojUtakmica.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate uneti naziv tima!");
                return;
            }

            Liga liga = (Liga) cmbLiga.getSelectedItem();
            String naziv = txtNaziv.getText();
            int brojPobeda = Integer.parseInt(txtBrojPobeda.getText());
            int brojUtakmica = Integer.parseInt(txtBrojUtakmica.getText());

            TableModelStavkeTima tm = (TableModelStavkeTima) tblStavke.getModel();

            t.setBrojIgraca(brojIgraca);
            t.setBrojPobeda(brojPobeda);
            t.setBrojUtakmica(brojUtakmica);
            t.setLiga(liga);
            t.setNaziv(naziv);
            t.setStavkeTima(tm.getLista());

            ClientController.getInstance().updateTim(t);
            FormPretragaTima fp = (FormPretragaTima) getParent();
            fp.refreshTable();
            JOptionPane.showMessageDialog(this, "Uspesno izmenjen tim.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
//            Logger.getLogger(FormDetaljiTima.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnDodajClanaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajClanaActionPerformed

        if (txtBrojDresa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti broj dresa i broj izostanaka!");
            return;
        }

        int brojDresa = Integer.parseInt(txtBrojDresa.getText());

        if (brojDresa < 1 || brojDresa > 99) {
            JOptionPane.showMessageDialog(this, "Broj dresa mora biti izmedju 1 i 99!");
            return;
        }

        Igrac igrac = (Igrac) cmbIgrac.getSelectedItem();
        String pozicija = (String) cmbPozicija.getSelectedItem();

        StavkaTima st = new StavkaTima(t, -1, brojDresa, pozicija, igrac);

        TableModelStavkeTima tm = (TableModelStavkeTima) tblStavke.getModel();

        if (tm.postojiBrojDresa(brojDresa)) {
            JOptionPane.showMessageDialog(this, "Vec postoji igrac sa tim brojem dresa!");
            return;
        }

        if (tm.postojiIgrac(igrac)) {
            JOptionPane.showMessageDialog(this, "Vec ste uneli tog igraca!");
            return;
        }

        tm.dodajStavku(st);

        brojIgraca = tm.getLista().size();
        txtBrojIgraca.setText(String.valueOf(brojIgraca));
    }//GEN-LAST:event_btnDodajClanaActionPerformed

    private void btnObrisiClanaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiClanaActionPerformed

        int row = tblStavke.getSelectedRow();

        if (row >= 0) {
            TableModelStavkeTima tm = (TableModelStavkeTima) tblStavke.getModel();
            tm.obrisiStavku(row);
            brojIgraca = tm.getLista().size();
            txtBrojIgraca.setText(String.valueOf(brojIgraca));
        }
    }//GEN-LAST:event_btnObrisiClanaActionPerformed


